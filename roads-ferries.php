      <p>Ferries were an important part of the Lowcountry transportation network because of the region’s many marshes and rivers. In 1686 the Lords Proprietors ordered anyone granted land with a river to operate a ferry. Combahee Ferry was established by 1715. The ferry and causeway were located half a mile upriver from where U.S. Highway 17 crosses the river today. A 1733 re-charter of Combahee Ferry moved the crossing to its present location.  Joseph Bryan was the first known ferry keeper.  As roads expanded and the popularity of the automobile increased, ferries became inconvenient. Combahee Ferry ceased to operate in 1927, when the first permanent bridge over the river was constructed.</p>

