    </header>
    <p>In 1735, Colonel John Mullryne of Beaufort constructed a store, lodging, and public house at Combahee Ferry.  By 1766, the tavern was known as Haymarket.  Mary “Polly” Keating owned and operated Haymarket tavern and store independent from her husband. Researchers do not know who operated the tavern during the 19th century, but a tavern or store served travelers at Combahee Ferry until 1927 when the first permanent bridge over the river was constructed. Archaeologists found garbage pits and a large privy or outhouse they believe served the tavern and ferry keeper’s house. Their excavations recovered pottery, bottle glass, buttons, animal bones, smoking pipes, lamp parts, nails, and window glass, and give us a glimpse into the household goods used by the ferry keeper or travelers during the early 19th century.</p>
