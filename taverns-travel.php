      <p>Taverns were rest stops for tired and hungry travelers, as well as important secular gathering places for meetings, entertainment, and conversation. The tavern keeper could be counted on for the latest news of the day. Public houses operated at all levels of society. Rural taverns usually catered to lower and middle class patrons, although travelers from all levels of society were welcome. Generally, tavern archaeological sites contain lots of artifacts associated with drinking and smoking. This was not the case with the tavern found in the CFHD. Perhaps this tavern was more of a stopover for travelers and not a gathering place for locals.</p>
